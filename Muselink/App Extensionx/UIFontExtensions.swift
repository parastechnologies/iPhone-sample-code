import UIKit

extension UIFont {
    class func AvenirLTPRo_Bold(size:CGFloat) -> UIFont {
        return UIFont.init(name: "AvenirNextLTPro-Bold", size: size)!
    }
    class func AvenirLTPRo_Demi(size:CGFloat) -> UIFont {
        return UIFont.init(name: "AvenirNextLTPro-Demi", size: size)!
    }
    class func AvenirLTPRo_Regular(size:CGFloat) -> UIFont {
        return UIFont.init(name: "AvenirNextLTPro-Regular", size: size)!
    }
    class func Avenir_Medium(size:CGFloat) -> UIFont {
        return UIFont.init(name: "Avenir-Medium", size: size)!
    }
}
